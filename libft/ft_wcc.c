#include "libft.h"

size_t	ft_wcc(char const *s, char c)
{
	size_t	count;

	count = 0;
	while (*s++)
	{
		if (*s == c && *(s - 1) != c)
			count++;
		else if (*s == '\0' && *(s - 1) != c)
			count++;
	}
	return (count);
}
